#!/usr/bin/env node
/**
 * Compile clean-question-bank JSON → lazy-loadable JS module (Phase 3).
 * Feature flag in cleanQuestionAdapter.js stays OFF until you enable it.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import { DOMAIN_4_OBJECTIVES } from './lib/cleanBankUtils.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const CLEAN_DIR = join(ROOT, 'data', 'clean-question-bank', 'domain-4')
const OUT = join(ROOT, 'src', 'data', 'ccnaCleanQuestionsDomain4.js')

function main() {
  const bank = {}
  for (const id of DOMAIN_4_OBJECTIVES) {
    const path = join(CLEAN_DIR, `${id}.json`)
    if (!existsSync(path)) continue
    const { questions } = JSON.parse(readFileSync(path, 'utf-8'))
    bank[id] = questions
  }

  const out = `// AUTO-GENERATED by scripts/compileCleanQuestionsModule.mjs — do not hand-edit.
// Student-facing Domain 4 clean question bank. Enable via cleanQuestionAdapter.js flag.
export const CLEAN_QUESTIONS_DOMAIN_4 = ${JSON.stringify(bank, null, 2)}
`
  writeFileSync(OUT, out)
  const total = Object.values(bank).reduce((n, qs) => n + qs.length, 0)
  console.log(`✓ Compiled ${total} clean questions → src/data/ccnaCleanQuestionsDomain4.js`)
}

main()
